// Function to initialize Web3 and request account access
const initializeWeb3 = async () => {
  if (typeof window.ethereum !== 'undefined') {
    const web3 = new Web3(window.ethereum);
    await window.ethereum.enable(); // Request account access
    return web3;
  } else {
    throw new Error('MetaMask or Ethereum provider not found');
  }
};


const ipfs = create({ host: 'localhost', port: 5001, protocol: 'http' }); // Use your local IPFS endpoint

async function convertFileToIPFS(file) {
  try {
    const formData = new FormData();
    formData.append('file', file);

    let ipfsURI = '';
    for await (const result of ipfs.addAll(formData)) {
      ipfsURI = `ipfs://${result.path}`;
    }

    if (!ipfsURI) {
      throw new Error('IPFS response is missing the expected path property.');
    }

    return ipfsURI;
  } catch (error) {
    console.error('Error converting file to IPFS:', error);
    throw error;
  }
}


